                  <br>
                  <p> Tidak Terlepas dari perkembangan zaman pada memasuki era revolusi industri 4.0 Kantor Pertanahan Musi Banyuasin mempunyai gagasan dalam membangun aplikasi Efektifitas Monotoring Hak Guna Usaha (HGU) seiring cukup banyaknya HGU pada Kabupaten Musi Banyuasin sehingga memudahkan dalam Memonitoring / Mengontrol agar memudahkan pemangku kepentingan mengambil kebijakan dan keputusan. </p>
                  <br>
                  <p> Dasar hukum mengenai jenis-jenis status tanah, termasuk HGU adalah Undang-Undang Republik Indonesia Nomor 5 Tahun 1960 tentang Peraturan Dasar Pokok-Pokok Agraria. Dalam regulasi tersebut disebutkan bahwa Hak Guna Usaha adalah hak untuk mengusahakan tanah yang dikuasai langsung oleh negara dalam jangka waktu tertentu untuk digunakan usaha pertanian, perikanan atau peternakan. Selain diatur UUPA, regulasi terkait HGU juga diatur dalam sejumlah aturan turunan seperti Peraturan Pemerintah (PP) Nomor 40 Tahun 1996 tentang Hak Guna Usaha, Hak Guna Bangunan, dan Hak Pakai Atas Tanah. </p>
                  <br>
                  <p> Salah satu jenis hak atas tanah adalah Hak Guna Usaha (HGU). Hak Guna Usaha (HGU) secara sederhana merupakan tanah yang peruntukkannya untuk hal-hal tertentu sebagaimana yang telah ditetapkan dalam undang-undang. Hal-hal tertentu yang dimaksud yaitu untuk pertanian, peternakan dan perikanan. </p>
                  <br>
                  <p> Lebih detail dijelaskan dalam pasal 28 UUPA Agraria 1960 bahwa Hak Guna Usaha adalah hak untuk mengusahakan tanah yang dikuasai langsung oleh Negara. Hak ini bisa untuk usaha pertanian, perikanan atau peternakan, dengan luas minimal 5 hektar. Masa berlakunya antara 25-35 tahun. HGU ini bisa dijual dan dialihkan kepada pihak lain. Hanya WNI dan Badan Hukum di Indonesia yang boleh memiliki HGU. HGU dapat dijadikan sebagai jaminan utang dengan dibebani hak tanggungan. Sebagaimana hak yang lain, hak ini pun dapat beralih atau dialihkan kepada pihak lain. </p>
                  <br>
                  <p>Jika luas HGU lebih dari 25 hektar, maka harus menggunakan mekanisme investasi modal yang layak dan teknik perusahaan yang baik, disesuaikan dengan perkembangan zaman. Untuk perusahaan yang memerlukan waktu yang lebih lama dapat diberikan hak guna usaha untuk waktu paling lama 35 tahun. Pemegang hak dapat meminta perpanjangan hak nya menjadi 25 tahun.
